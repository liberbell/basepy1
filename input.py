import urllib.request
import re

print('Enter URL')
URL = input('>> ')

if re.match(r"^https?:\/\/", URL):
    print(URL)
    try:
        res = urllib.request.urlopen(URL)
        res.close()
    except urllib.request.HTTPError as reqerr:
        print('Not found: ', URL)
        print('Error Code: ' + str(reqerr.code))

    except urllib.request.URLError as reqerr:
        print('Not found: ', URL)
        # print('Error Reason: ' + reqerr.reason)
        # print(type(reqerr.reason))
        print('Error reason: ' +str(reqerr.reason))

else:
    print('Wrong URL')

